const { describe, it, beforeEach, afterEach } = require('node:test');
const assert = require('node:assert/strict');
const fs = require('fs');
const os = require('os');
const path = require('path');
const { resolveFormats, processTemplate, generateAllFormats } = require('../lib/install');
const { detectStack } = require('../lib/detect');
const { scanAll } = require('../lib/scan');

const FIXTURES = path.join(__dirname, 'fixtures');
const NODE_PROJECT = path.join(FIXTURES, 'node-project');

describe('resolveFormats', () => {
  it('returns all 17 format keys for "all"', () => {
    const formats = resolveFormats('all');
    assert.equal(formats.length, 17);
  });

  it('returns 6 popular formats for "popular"', () => {
    const formats = resolveFormats('popular');
    assert.equal(formats.length, 6);
    assert.ok(formats.includes('claude'));
    assert.ok(formats.includes('cursor'));
  });

  it('returns single format for a known name', () => {
    const formats = resolveFormats('claude');
    assert.deepEqual(formats, ['claude']);
  });

  it('warns and falls back to claude for unknown format', () => {
    const warnings = [];
    const origWarn = console.warn;
    console.warn = (msg) => warnings.push(msg);

    const formats = resolveFormats('garbage');

    console.warn = origWarn;
    assert.deepEqual(formats, ['claude']);
    assert.ok(warnings.length > 0, 'Should have printed a warning');
    assert.ok(warnings[0].includes('Unknown format'));
  });
});

describe('processTemplate', () => {
  it('replaces placeholders', () => {
    const template = '# {{PROJECT_NAME}}\n{{PROJECT_DESCRIPTION}}';
    const stack = { name: 'my-app', description: 'A cool app' };
    const result = processTemplate(template, stack, null);
    assert.ok(result.includes('# my-app'));
    assert.ok(result.includes('A cool app'));
  });

  it('includes sections when condition is true', () => {
    const template = 'before\n{{#IF_NODE}}Node content{{/IF_NODE}}\nafter';
    const stack = { isNode: true, name: 'test', description: '' };
    const result = processTemplate(template, stack, null);
    assert.ok(result.includes('Node content'));
  });

  it('removes sections when condition is false', () => {
    const template = 'before\n{{#IF_NODE}}Node content{{/IF_NODE}}\nafter';
    const stack = { isNode: false, name: 'test', description: '' };
    const result = processTemplate(template, stack, null);
    assert.ok(!result.includes('Node content'));
    assert.ok(result.includes('before'));
    assert.ok(result.includes('after'));
  });
});

describe('generateAllFormats', () => {
  let tmpDir;

  beforeEach(() => {
    tmpDir = fs.mkdtempSync(path.join(os.tmpdir(), 'ccs-test-'));
    // Copy fixture package.json so detection works
    fs.copyFileSync(
      path.join(NODE_PROJECT, 'package.json'),
      path.join(tmpDir, 'package.json')
    );
  });

  afterEach(() => {
    fs.rmSync(tmpDir, { recursive: true, force: true });
  });

  it('generates files in normal mode', () => {
    const stack = detectStack(tmpDir);
    const generated = generateAllFormats(tmpDir, stack, null, 'claude');
    assert.ok(generated.includes('claude'));
    assert.ok(fs.existsSync(path.join(tmpDir, 'CLAUDE.md')));
  });

  it('dry-run mode does not write files', () => {
    const stack = detectStack(tmpDir);
    const generated = generateAllFormats(tmpDir, stack, null, 'claude', false, true);
    assert.ok(generated.includes('claude'));
    assert.ok(!fs.existsSync(path.join(tmpDir, 'CLAUDE.md')), 'Should not create file in dry-run');
  });

  it('update mode skips files that do not exist', () => {
    const stack = detectStack(tmpDir);
    const generated = generateAllFormats(tmpDir, stack, null, 'claude', true);
    assert.equal(generated.length, 0, 'Should skip non-existing files in update mode');
    assert.ok(!fs.existsSync(path.join(tmpDir, 'CLAUDE.md')));
  });

  it('update mode refreshes files that exist', () => {
    const stack = detectStack(tmpDir);
    // First create the file
    generateAllFormats(tmpDir, stack, null, 'claude');
    assert.ok(fs.existsSync(path.join(tmpDir, 'CLAUDE.md')));

    // Now update — should succeed since file exists
    const generated = generateAllFormats(tmpDir, stack, null, 'claude', true);
    assert.ok(generated.includes('claude'));
  });

  it('generated files include version stamp', () => {
    const stack = detectStack(tmpDir);
    generateAllFormats(tmpDir, stack, null, 'claude');
    const content = fs.readFileSync(path.join(tmpDir, 'CLAUDE.md'), 'utf-8');
    assert.ok(content.startsWith('<!-- Generated by claude-code-standards v'));
  });
});
